#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char	count;

	count = '0';
	while (count <= '9')
	{
		ft_putchar(count);
		count++;
	}
}
